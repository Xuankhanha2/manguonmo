    public void loadData() throws SQLException{
        String sql = "select * from hoadon";
        result = connect.getData(sql);
        DefaultTableModel model = (DefaultTableModel)tableNV.getModel();
        model.setRowCount(0);
        while(result.next()){
            model.addRow(new Object[]{result.getString("maHD"),
                result.getString("maNV"),
                result.getString("loaiHD"),
                result.getString("ngayLap"),
                result.getString("ngayGiao"),
                result.getString("dienGiai")
            });
        }
    }
